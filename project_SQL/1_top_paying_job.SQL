/*
Question: What are the top_paying data analyst job
- Identify the top 10 highest-paying job Data Analyst role that are available remotely.
- Focuses on job postings with specified salaries (remove nulls)
- Why? Highlight the top-paying opportunities for Data Analyst, offering insights into recommendations
*/

SELECT
    job_id,
    job_title,
    job_location,
    job_schedule_type,
    salary_year_avg,
    job_posted_date,
    name as company_name
FROM
    job_postings_fact
LEFT JOIN company_dim on job_postings_fact.company_id = company_dim.company_id
WHERE
    job_title_short = 'Data Analyst' and
    job_location = 'Anywhere' and 
    salary_year_avg is not null
ORDER BY
    salary_year_avg DESC
LIMIT 10